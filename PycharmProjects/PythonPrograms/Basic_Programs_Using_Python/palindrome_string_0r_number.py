string=input("enter the string:")
rev_string=string[::-1]
if rev_string==string:
    print("string is palindrome")
else:
    print("string is not a palindrome")

number=input("enter the number:")
rev_num=number[::-1]
if rev_num==number:
    print("num is palindrome")
else:
    print("num is not a palindrome")